         <li class="para1"> If tie still remains, then the candidate having higher marks in Physics (For Engineering), Chemistry (for Medical) will be taken into account.</li>
         <li class="para1"> If tie still remains, then the candidates with less number of negative answers will be preferred.</li>
         <li class="para1"> If tie still remains, then the candidates who took minimum time will be preferred.</li>
         <li class="para1"> If tie still not resolved, then the older aged candidate will be preferred.</li>
